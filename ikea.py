# Beschreibung
#     Ikea-Lampe aufbauen nach Anleitung
# Deklaration und Definition der lokalen Größen
#     Sockel, Mittelstange (1), Mittelstange mit Fassung (2), Mittelstange (3), Kabelklemme, Lampenschirm, Gabelung, Schraubendeckel
# Algorithmuskern
#     Mittelstange (1) auf Sockel schrauben und Mittelstangen (1) mit Mittelstange (2) verschrauben
#     FALLS Mittelstange (1) und (2) verschraubt
#     DANN
#       Kabelklemme anbringen und Gabelung auf Fassung von Mittelstange (2) schrauben
#       FALLS Kabelklemme und Gabelung verschraubt
#       DANN
#           Mittelstange(3) mit Gabelung verschrauben und Schraubendeckel abschrauben
#           FALLS Mittelstange(3) verschraubt und Schraubendeckel abgeschraubt
#           DANN
#               Lampenschirm auf Mittelstange(3) aufstecken, mit Schraubendeckel fixieren und nach unten spannen
#               FALLS Lampenschirm auf Mittelstange (3) angebracht, fixiert mit Schraubendeckel und gespannt
#               DANN
#                   Ausgabe "Sie haben die Lampe korrekt aufgebaut"
#               SONST Ikea()
#               ENDE FALLS
#           SONST Ikea()
#           ENDE FALLS
#       SONST Ikea()
#       ENDE FALLS
#     SONST Ikea()
#     ENDE FALLS



